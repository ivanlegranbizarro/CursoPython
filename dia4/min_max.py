# Min y Max funcionan los valores más altos y bajos de una lista
# aceptan valores numéricos y alfabéticos

nombres = ["Pepe", "Juan", "Luis"]

print(min(nombres))
print(max(nombres))

edades = [33, 38, 42]

print(min(edades))
print(max(edades))

print(f"De los valores {edades} el menor es {min(edades)} y el mayor es {max(edades)}")
